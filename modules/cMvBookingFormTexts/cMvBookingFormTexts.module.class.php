class cMvBookingFormTexts extends cMvBookingFormTextsMaster {
		//////////////////////////////////////////////////////////////////////
		// Indexierte Liste laden.
		//////////////////////////////////////////////////////////////////////
		function loadIndexedList() {
				$data = $this->loadList();
				
				$retval = array();
				
				foreach($data as $d) {
						$retval[$d['internal_identifier']] = $d['field_value'];
				}
				
				return $retval;
		}
}

