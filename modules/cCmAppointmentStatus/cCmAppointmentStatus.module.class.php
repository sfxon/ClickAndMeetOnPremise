class cCmAppointmentStatus extends cCmAppointmentStatusMaster {
		//////////////////////////////////////////////////////////////////////
		// Indexierte Liste laden.
		//////////////////////////////////////////////////////////////////////
		function loadIndexedList() {
				$data = $this->loadList();
				
				$retval = array();
				
				foreach($data as $d) {
						$retval[$d['id']] = $d;
				}
				
				return $retval;
		}
}

